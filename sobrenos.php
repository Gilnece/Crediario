    <h1>Sobre nós</h1>

    <p>
      Este projeto foi desenvolvido para a disciplina de Desenvolvimento Web do curso de Engenharia da Computação, no 9º período. Ele tem como objetivo medir e aprimorar os conhecimentos dos estudantes em programação, explorando amplamente o uso de tecnologias e técnicas essenciais, tais como HTML, CSS, PHP, além de outras linguagens e frameworks comumente utilizados no desenvolvimento web. Ao longo do projeto, os alunos são incentivados a criar e aprimorar suas habilidades na construção de páginas e aplicações web, aplicando conceitos de estruturação de conteúdo, estilização visual, interação com o usuário e manipulação de dados. O projeto busca proporcionar uma experiência prática e abrangente no desenvolvimento web, preparando os estudantes para os desafios enfrentados no campo da Engenharia da Computação.    
    </p>
    <h2>O que é HTML?</h2>
    <p>
      HTML (HyperText Markup Language) é a linguagem padrão para a criação de páginas da web. Ela define a estrutura e o conteúdo de uma página usando elementos de marcação, como tags, que indicam ao navegador como exibir o conteúdo.
    </p>

    <h2>O que é CSS?</h2>
    <p>
      CSS (Cascading Style Sheets) é uma linguagem de estilo usada para controlar a apresentação e o layout de elementos HTML. Com CSS, é possível definir cores, fontes, espaçamento, posicionamento e outros estilos visuais das páginas da web.
    </p>

    <h2>O que é PHP?</h2>
    <p>
      PHP é uma linguagem de programação amplamente utilizada para desenvolvimento web do lado do servidor. Ela permite criar páginas dinâmicas, processar formulários, acessar bancos de dados e muito mais. O PHP é executado no servidor antes que a página seja enviada para o navegador.
    </p>
